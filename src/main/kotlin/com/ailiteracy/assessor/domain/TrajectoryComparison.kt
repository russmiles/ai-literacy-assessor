/**
 * TrajectoryComparison captures the delta between the current assessment and the most
 * recent previous one for the same team. Its purpose is to make progress visible —
 * a single assessment tells you where you are, but trajectory tells you whether you
 * are moving and how fast.
 *
 * The [disciplineDeltas] map gives change in score for each of the three ALCI
 * disciplines (context engineering, constraint, guardrail). Positive values represent
 * growth; negative values represent regression; zero means no change. A team that has
 * advanced one full level but regressed on a discipline is surfaced here — not hidden
 * in an aggregated score.
 *
 * The [narrative] field is a short prose summary generated by the fast model. It
 * interprets the deltas in human terms: what the team accomplished since the last
 * assessment, where momentum is strong, and where to be watchful.
 *
 * When no previous assessment exists, this type is still produced — with both levels
 * identical and deltas at zero — so that the [assembleDocument] action has a consistent
 * input regardless of whether this is a first or subsequent assessment. The narrative
 * in that case notes it is the team's first assessment.
 */
package com.ailiteracy.assessor.domain

/**
 * The comparison between a previous assessment and the current one.
 *
 * [disciplineDeltas] maps discipline names ("contextEngineering", "constraint",
 * "guardrail") to the numeric change in score since the previous assessment.
 * The [narrative] field is a 2–3 sentence prose interpretation suitable for the
 * opening section of the assessment document's trajectory section.
 */
data class TrajectoryComparison(
    val previousLevel: LiteracyLevel,
    val currentLevel: LiteracyLevel,
    // Maps discipline name to change in score (positive = growth, negative = regression)
    val disciplineDeltas: Map<String, Int>,
    val narrative: String
)
